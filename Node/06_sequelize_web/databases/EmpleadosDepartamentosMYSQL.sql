--
-- TABLA depart
--
CREATE TABLE depart (
 depart_no  TINYINT UNSIGNED NOT NULL PRIMARY KEY, -- Agregado UNSIGNED para coincidir
 dnombre    VARCHAR(50),                          -- Aumentado por seguridad
 loc        VARCHAR(50)                           -- Aumentado por seguridad
) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

