 depart_no   TINYINT UNSIGNED NOT NULL, -- Ahora coincide perfectamente con depart
 FOREIGN KEY (depart_no) REFERENCES depart(depart_no)
) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

